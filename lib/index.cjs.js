    var a = document.createElement("a");
    a.style.display = "none";
    // 火狐兼容
    if (getBrowser().type === "Firefox")
        a.target = "_blank";
    a.href = url;
    a.download = fileName;
    a.rel = "noopener noreferrer"; // 使用target="_blank"时，添加rel="noopener noreferrer" 堵住钓鱼安全漏洞 防止新页面window指向之前的页面
